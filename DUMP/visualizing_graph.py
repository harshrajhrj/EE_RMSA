# Create graph from adjacency matrix
G = nx.Graph()

num_nodes = matrix.shape[0]
for i in range(num_nodes):
    G.add_node(i)

for i in range(num_nodes):
    for j in range(i + 1, num_nodes):
        if matrix[i, j] != 0:
            G.add_edge(i, j, weight=matrix[i, j])

# Draw the graph with edge labels
